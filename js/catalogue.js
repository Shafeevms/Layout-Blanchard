const accordionList = (data, year) => {
  let li = data.accordion[year].artists.reduce((acc, item) => {
    acc +=  `<li class="content__item"><a href="#" class="content__link">${item}</a></li>`;
    return acc;
  }, '');
  return `<ul class="content__list">${li}</ul>`;
}

const accordionItem = data => {
  return Object.keys(data.accordion).reduce((acc, date) => {
    acc += `<button class="accordion__btn">${data.accordion[date].date}</button>
    <div class="accordion__content content">${accordionList(data, date)}</div>`
    return acc;
  }, '');
}

const render = (string, selector) => {
  document.querySelector(selector).innerHTML = string;
}

document.querySelector('.catalogue').addEventListener('click', function(e) {
  if (e.target.classList.contains('catalogue__img')) {
    switch(e.target.dataset.lg) {
      case 'storeFR':
        pageData = storeFR;
        break;
      case 'storeDE':
        pageData = storeDE;
        break;
      case 'storeIT':
        pageData = storeIT;
        break;
      case 'storeBG':
        pageData = storeBG;
        break;
        default:
          pageData = storeRU;
        break;
    }

    render(catalogueTemplate(pageData), '.catalogue')
    $( "#accordion" ).accordion();
  }
})

const catalogueTemp = (data) => {

  if (data.length === 0) {
  return `
        <img src="./img/modal/wrong.png" alt="" class="article__img">
        <h2 class="article__artist reset"></h2>
        <h3 class="article__years reset"></h3>
        <p class="article__description reset">
        something goes wrong...
        </p>`

    } else if (!data.length) {
      return `
      <img src="./img/catalogue/${data.accordionAPI[0].uri}" alt="${data.accordionAPI[0].artistName}" class="article__img">
      <h2 class="article__artist reset">${data.accordionAPI[0].artistName}</h2>
      <h3 class="article__years reset">${data.accordionAPI[0].years}</h3>
      <p class="article__description reset">
      ${data.accordionAPI[0].description}
      </p>`
    } else {
      const [filtered] = data;
      return `
      <img src="./img/catalogue/${filtered.uri}" alt="${filtered.artistName}" class="article__img">
      <h2 class="article__artist reset">${filtered.artistName}</h2>
      <h3 class="article__years reset">${filtered.years}</h3>
      <p class="article__description reset">
      ${filtered.description}
      </p>`
    }
}

